package model;

public class Book {
	private String boek;
	private String auteur;
	
	
	public Book() {
		super();
	}
	public String getBoek() {
		return boek;
	}
	public void setBoek(String boek) {
		this.boek = boek;
	}
	public String getAuteur() {
		return auteur;
	}
	public void setAuteur(String auteur) {
		this.auteur = auteur;
	}

}
